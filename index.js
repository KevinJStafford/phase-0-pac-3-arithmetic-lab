function add(num1, num2) {
    return num1 + num2;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b){
    return a / b;
}

function increment(a) {
    return a + 1;
}

function decrement(a) {
    return a - 1;
}

function makeInt(a) {
    return parseInt(a, 10);
}

function preserveDecimal(a) {
    return parseFloat(a);
}